namespace Task2
{
    /// <summary>
    /// Represents the possible types of a triangle based on its sides.
    /// </summary>
    public enum TriangleType
    {
        /// <summary>
        /// Indicates an equilateral triangle, where all three sides are of equal length.
        /// </summary>
        Equilateral = 0,

        /// <summary>
        /// Indicates an isosceles triangle, where at least two sides are of equal length.
        /// </summary>
        Isosceles = 1,

        /// <summary>
        /// Indicates a scalene triangle, where all three sides are of different lengths.
        /// </summary>
        Scalene = 2
    }
}
